// Pixel-art sprite system for Dungeon RPG
// Sprites are 8×8 pixel grids rendered scaled to the entity/tile target size.
// Palette index 0 = transparent.

export interface SpriteData {
  frames: number[][][]; // [frame][row][col]
  palette: string[];    // [0]=transparent, [1..n]=hex colours
}

/** Draw one frame of a sprite into the given world-space rectangle. */
export function drawSprite(
  ctx: CanvasRenderingContext2D,
  x: number, y: number,
  w: number, h: number,
  sprite: SpriteData,
  frame = 0,
): void {
  const f = sprite.frames[Math.abs(frame) % sprite.frames.length];
  const rows = f.length;
  const cols = f[0].length;
  const pw = w / cols;
  const ph = h / rows;
  for (let r = 0; r < rows; r++) {
    for (let c = 0; c < cols; c++) {
      const ci = f[r][c];
      if (ci === 0) continue;
      ctx.fillStyle = sprite.palette[ci];
      ctx.fillRect(
        Math.floor(x + c * pw),
        Math.floor(y + r * ph),
        Math.ceil(pw) + 1,
        Math.ceil(ph) + 1,
      );
    }
  }
}

/** Compute animation frame index from elapsed time in ms. */
export function animFrame(sprite: SpriteData, timeMs: number, fps = 4): number {
  return Math.floor((timeMs / 1000) * fps) % sprite.frames.length;
}

