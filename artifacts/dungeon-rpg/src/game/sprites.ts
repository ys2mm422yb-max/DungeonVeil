// High-quality pixel-art sprite system for Dungeon Veil.
// All sprites are 8×8 or 10×10 grids rendered scaled to the target size.
// Palette index 0 = transparent.

export interface SpriteData {
  /** [frame][row][col] */
  frames: number[][][];
  /** [0]=transparent, [1..n]=hex colours */
  palette: string[];
  /** Optional origin offset in tile fractions (0..1). Used for alignment. */
  origin?: { x: number; y: number };
}

/** Draw one frame of a sprite into the given world-space rectangle. */
export function drawSprite(
  ctx: CanvasRenderingContext2D,
  x: number, y: number,
  w: number, h: number,
  sprite: SpriteData,
  frame = 0,
  flipX = false,
): void {
  const f = sprite.frames[Math.abs(frame) % sprite.frames.length];
  const rows = f.length;
  const cols = f[0].length;
  const pw = w / cols;
  const ph = h / rows;
  const origin = sprite.origin ?? { x: 0, y: 0 };
  const offX = origin.x * w;
  const offY = origin.y * h;

  for (let r = 0; r < rows; r++) {
    for (let c = 0; c < cols; c++) {
      const ci = f[r][flipX ? cols - 1 - c : c];
      if (ci === 0) continue;
      ctx.fillStyle = sprite.palette[ci];
      ctx.fillRect(
        Math.floor(x + c * pw - offX),
        Math.floor(y + r * ph - offY),
        Math.ceil(pw) + 1,
        Math.ceil(ph) + 1,
      );
    }
  }
}

/** Compute animation frame index from elapsed time in ms. */
export function animFrame(sprite: SpriteData, timeMs: number, fps = 4): number {
  if (sprite.frames.length <= 1) return 0;
  return Math.floor((timeMs / 1000) * fps) % sprite.frames.length;
}

/** 1D bob offset for idle/walk bounce. */
export function bobOffset(phaseMs: number, amplitude = 1.5): number {
  return Math.sin(phaseMs / 160) * amplitude;
}

